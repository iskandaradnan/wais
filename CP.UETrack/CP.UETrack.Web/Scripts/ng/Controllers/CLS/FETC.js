function LinkClicked(id) {
    $(".content").scrollTop(1);
    $('.nav-tabs a:first').tab('show');
    $("#formFETC :input:not(:button)").parent().removeClass('has-error');
    $("div.errormsgcenter").text("");
    $('#errorMsg').css('visibility', 'hidden');
    var action = "";
    $('#primaryID').val(id);
    var hasEditPermission = Enumerable.From(JSON.parse($('#ActionPermissionValues').val())).Any("$.ActionPermissionName=='Edit'");
    var hasViewPermission = Enumerable.From(JSON.parse($('#ActionPermissionValues').val())).Any("$.ActionPermissionName=='View'");
    var hasDeletePermission = Enumerable.From(JSON.parse($('#ActionPermissionValues').val())).Any("$.ActionPermissionName=='Delete'");

    if (hasEditPermission) {
        action = "Edit"

    }
    else if (!hasEditPermission && hasViewPermission) {
        action = "View"
    }
    if (action == "Edit" && hasDeletePermission) {
        $('#btnDelete').show();
    }

    if (action == 'View') {
        $("#formFETC :input:not(:button)").prop("disabled", true);
    } else {
        $('#btnEdit').show();
        $('#btnNextScreenSave').show();
    }
    $('#spnActionType').text(action);

    var primaryId = $('#primaryID').val();
    if (primaryId != null && primaryId != "0") {
        $.get("/api/FETC/Get/" + primaryId)
            .done(function (result) {
                var getResult = JSON.parse(result);
                $('#txtItemCode').val(getResult.ItemCode);
                $('#txtItemDescription').val(getResult.ItemDescription);
                $('#ddlItemType').val(getResult.ItemType);
                $('#ddlStatus').val(getResult.Status);
                $('#txtQuantity').val(getResult.Quantity);
                $('#txtEffectiveFrom').val(getResult.EffectiveFrom);

                let monthNames = ["Zero", "Jan", "Feb", "Mar", "Apr",
                    "May", "Jun", "Jul", "Aug",
                    "Sep", "Oct", "Nov", "Dec"];

                var fromDate = $('#txtEffectiveFrom').val();
                
                var year = fromDate.slice(0, 4);
                var month = fromDate.slice(5, 7);
                var date = fromDate.slice(8, 10);
                var monthName = '';

                if (month >= 10) {
                    monthName = monthNames[fromDate.slice(5, 7)];
                }
                else {
                    monthName = monthNames[fromDate.slice(6, 7)];
                }

                var dataformate = date + "-" + monthName + "-" + year;
                $('#txtEffectiveFrom').val(dataformate);

                if (getResult.EffectiveTo != null) {
                    $('#txtEffectiveTo').val(getResult.EffectiveTo);
                    var toDate = $('#txtEffectiveTo').val();
                                        
                    var year = toDate.slice(0, 4);
                    var month = toDate.slice(5, 7);
                    var date = toDate.slice(8, 10);
                    var monthName = ''
                    
                    if (month >= 10) {
                        monthName = monthNames[toDate.slice(5, 7)];
                    }
                    else {
                        monthName = monthNames[toDate.slice(6, 7)];
                    }

                    
                    var dataformate2 = date + "-" + monthName + "-" + year;
                    $('#txtEffectiveTo').val(dataformate2);
                    $("#txtEffectiveTo").prop('disabled', false);
                }
                else {
                    $('#txtEffectiveTo').val('');
                    $("#txtEffectiveTo").prop('disabled', true);
                }

                
                fillAttachment(getResult.AttachmentList);
                

            })
            .fail(function () {
                $('#myPleaseWait').modal('hide');
                $("div.errormsgcenter").text(Messages.COMMON_FAILURE_MESSAGE);
                $('#errorMsg').css('visibility', 'visible');
            });
    }
    else {
        $('#myPleaseWait').modal('hide');

    }
}

